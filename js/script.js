function changeColorToGreen() {
    document.documentElement.style.setProperty('--color-blue', '#18bd9b');
}

function changeColorToOrange() {
    document.documentElement.style.setProperty('--color-blue', '#f05454');
}

function changeColorToDark() {
    document.documentElement.style.setProperty('--color-blue', '#2a5172');
}